    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int input1 = Integer.parseInt(jTextField1.getText());
        int input2 = Integer.parseInt(jTextField2.getText());
        int input3 = Integer.parseInt(jTextField3.getText());
        int input4 = Integer.parseInt(jTextField4.getText());
        int input5 = Integer.parseInt(jTextField5.getText());
        int input6 = Integer.parseInt(jTextField6.getText());
        double output = ((input1+input2+input3+input4)*3.92+(input5+input6)*4.07)/(4*3.92 + 2*4.07);
        int finalOutput = (int) Math.round(output);
        String result = "";
        if(finalOutput < 4)
        {
            result = "Poor";
            pf.setForwardexternal(1);
        }
        else if (finalOutput < 6)
        {
            result = "Fair";
            pf.setForwardexternal(2);
        }
        else
        {
            result = "Good";
            pf.setForwardexternal(3);
        }
        jTextField7.setText(result);
    }//GEN-LAST:event_jButton2ActionPerformed

