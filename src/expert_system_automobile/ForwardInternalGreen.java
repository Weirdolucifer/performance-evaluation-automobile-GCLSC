    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int input1 = Integer.parseInt(jTextField12.getText());
        int input2 = Integer.parseInt(jTextField2.getText());
        int input3 = Integer.parseInt(jTextField3.getText());

        int input5 = Integer.parseInt(jTextField5.getText());
        int input6 = Integer.parseInt(jTextField6.getText());

        int input4 = Integer.parseInt(jTextField4.getText());
        int input8 = Integer.parseInt(jTextField8.getText());
        int input9 = Integer.parseInt(jTextField9.getText());

        int input10 = Integer.parseInt(jTextField10.getText());
        int input11 = Integer.parseInt(jTextField11.getText());

        double output = ((input1+input2+input3)*3.85 + (input5+input6)*3.95 + (input4+input8+input9)*4.19 + (input10+input11)*3.91 )/(3*3.85 + 2*3.95 + 3*4.19 + 2*3.91);

        int finalOutput = (int) Math.round(output);
        String result = "";
        if(finalOutput < 4)
        {
            result = "Poor";
            pf.setForwardinternalgreen(1);
        }
        else if (finalOutput < 6)
        {
            result = "Fair";
            pf.setForwardinternalgreen(2);
        }
        else
        {
            result = "Good";
            pf.setForwardinternalgreen(3);
        }
        jTextField7.setText(result);
    }//GEN-LAST:event_jButton2ActionPerformed

