    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        pf.CalculateForwardPerformance();
        pf.CalculateReversePerformance();
        pf.CalculatePerformance();
        
        String forwardResult = "";
        String backwardResult = "";
        String Result = "";
        
        if(pf.getForwardexternal() == 0) forwardResult = "Reevaluate Forward External";
        else if(pf.getForwardinternalgreen() == 0) forwardResult = "Reevaluate Forward Internal Green";
        else if(pf.getForwardinternaltraditional() == 0) forwardResult = "Reevaluate Forward Internal Traditional";
        else if(pf.getReverseexternal() == 0) backwardResult="Reevaluate Reverse External";
        else if(pf.getReverseinternal() == 0) backwardResult="Reevaluate Reverse Internal";
        
        else
        {
            if(pf.getForwardresult() == 1) forwardResult = "Poor";
            else if(pf.getForwardresult() == 2) forwardResult = "Fair";
            else if(pf.getForwardresult() == 3) forwardResult = "Good";
            else forwardResult = "Try again";

            if(pf.getReverseresult() == 1) backwardResult = "Poor";
            else if(pf.getReverseresult() == 2) backwardResult = "Fair";
            else if(pf.getReverseresult() == 3) backwardResult = "Good";
            else backwardResult = "Try again";

            if(pf.getResult() == 1) Result = "Poor";
            else if(pf.getResult() == 2) Result = "Fair";
            else if(pf.getResult() == 3) Result = "Good";
            else Result = "Try again";
        }
        
        jTextField1.setText(forwardResult);
        jTextField2.setText(backwardResult);
        jTextField3.setText(Result);
        
    }//GEN-LAST:event_jButton1ActionPerformed

